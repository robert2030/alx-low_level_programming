#include <stdio.h>
/**
 *addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * Subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 *modulas
 */
int mod(int a, int b)
{
	return (a % b);
}
